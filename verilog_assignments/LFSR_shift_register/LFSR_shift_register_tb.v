module lfsr_shift_register_tb;

reg rst_n,clk;

wire [3:0]dout;

lfsr_shift_register dut (.rst_n(rst_n),.clk(clk),.dout(dout));

always #5 clk = ~clk;

initial begin 

    $dumpfile(" lfsr_shift_register.vcd");
    $dumpvars(0, lfsr_shift_register_tb);

    $monitor("$time=%0t | rst_n=%b,clk=%b | dout =%b",$time,rst_n,clk,dout);

end

initial begin

    clk =0;
    rst_n=0;

    @(posedge clk);
    rst_n=1;

    repeat(10)@(posedge clk);

    $finish;

end
endmodule
















































































































































































































































































